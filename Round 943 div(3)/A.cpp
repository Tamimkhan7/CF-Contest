void solve()
{
    int x;
    cin >> x;
    int mx = INT_MIN, ans;
    for (int i = 1; i < x; i++)
    {
        int p = __gcd(x, i) + i;
        if (p > mx)
        {
            mx = p;
            ans = i;
        }
    }
    cout << ans << '\n';
}
